    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String eventId=jTextField1.getText();
        String eventName=jTextField2.getText();
         String venue=jComboBox1.getSelectedItem().toString();
         String date;
         date=((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText();
         el.add(new Event(eventId,eventName,venue,date));
         dtm=(DefaultTableModel)jTable1.getModel();
         dtm.setRowCount(0);
         Object r[]=new Object[4];
         for(Event x:el ){
             r[0]= x.eventId;
             r[1]=x.eventName;
             r[2]=x.venue;
             r[3]=x.date;
             dtm.addRow(r);
         }
         jTextField1.setText("");
         jTextField2.setText("");
         jComboBox1.setSelectedIndex(0);
         jDateChooser1.setCalendar(null);
         
    }//GEN-LAST:event_jButton1ActionPerformed

