    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        dtm=(DefaultTableModel)jTable1.getModel();
        String eventId=jTextField1.getText();
        String eventName=jTextField2.getText();
         String venue=jComboBox1.getSelectedItem().toString();
         String date;
         date=((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText();
         int i=jTable1.getSelectedRow();
         el.get(i).eventId=eventId;
         el.get(i).eventName=eventName;
         el.get(i).venue=venue;
         el.get(i).date=date;
         dtm.setValueAt(eventId, i, 0);
         dtm.setValueAt(eventName, i, 1);
         dtm.setValueAt(venue, i, 2);
         dtm.setValueAt(date, i, 3);
         
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try {
            FileWriter fw = new FileWriter("events.txt");
            BufferedWriter bw = new BufferedWriter(fw);
            for (Event e : el) {
                bw.write(e.eventId + "," + e.eventName + "," + e.venue + ","+ "," + e.date);
                bw.newLine();
            }
            bw.close();
            fw.close();
            JOptionPane.showMessageDialog(this, "Data saved successfully!");
        } catch (IOException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error saving data!");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

